// 01. COMPILAZIONE

void compilazione ( void )
{

    // titolo
    printf ( "\t1. La compilazione\n\n" ) ;

    // testo
    printf ( "\t\tLe fasi della compilazione sono: \n\n" ) ;
    printf ( "\t\t1. il preprocessore C esegue le istruzioni con # (include, define, ecc.);\n" ) ;
    printf ( "\t\t2. il compilatore traduce il codice sorgente in codice assembly;\n" ) ;
    printf ( "\t\t3. l'assembler traduce il codice assembly in codice oggetto (.obj);\n" ) ;
    printf ( "\t\t4. il linker trasforma il codice oggetto in eseguibile (.exe), collegando le librerie, ecc.;\n" ) ;
    printf ( "\t\t5. il loader scrive il codice in linguaggio macchina nella memoria principale (ram).\n\n" ) ;

}
