// 01. COMPILAZIONE

void compilazione ( void )
{

    // titolo
    puts ( "\t1. La compilazione\n" ) ;

    // testo
    puts ( "\t\tLe fasi della compilazione sono: \n" ) ;
    puts ( "\t\t1. il preprocessore C esegue le istruzioni con # (include, define, ecc.);" ) ;
    puts ( "\t\t2. il compilatore traduce il codice sorgente in codice assembly;" ) ;
    puts ( "\t\t3. l'assembler traduce il codice assembly in codice oggetto (.obj);" ) ;
    puts ( "\t\t4. il linker trasforma il codice oggetto in eseguibile (.exe), collegando le librerie, ecc.;" ) ;
    puts ( "\t\t5. il loader scrive il codice in linguaggio macchina nella memoria principale (ram).\n" ) ;

}
// fine
